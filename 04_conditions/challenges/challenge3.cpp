#include<iostream>
using namespace std;

int main()
{
    int temperature;

    cout << "Enter tea water temperature: ";
    cin >> temperature;

    if(temperature > 100){
        cout << "Too Hot!" << endl;
    }
    else if (temperature >= 80)
    {
        cout << "Perfect Temperature." << endl;
    }
    else{
        cout << "Too Cold!" << endl;
    }
    


    return 0;
}